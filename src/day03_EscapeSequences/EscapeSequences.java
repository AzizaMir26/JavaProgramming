package day03_EscapeSequences;
/*
escape sequences :must be given with double quote
\n: starts new line
\t paragraph space(tab)
\\ single back slash
\" double quote

 */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");
        System.out.println("----------------------------");
        System.out.println("Hello Cydeo \nHow are you all today? \nIts nice to see you all!\nWhat class do we have next week?");
        System.out.println("---------------------------");
        System.out.println("\tJava is Cool Programming Language");
        System.out.println("-------------------------");
        System.out.println("/\\");//in order   to print one  backward slash we need to give the backward slashes
        System.out.println("---------------------------");
        System.out.println("My favourite Tv Show is \" Game of Throne\""); // in order to pring double" we need to use \




    }
}
