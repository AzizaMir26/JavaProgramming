package day09_IfStatements;

public class MaximumNumber {
    public static void main(String[] args) {
        int n1= 100;
        int n2 = 1000;

        if(n1>n2){
            System.out.println(n1+ " is the maximum number");
        }else{
            System.out.println(n2 + " is the maximum number");
        }
    }
}
