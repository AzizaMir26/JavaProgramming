package day17_While_DoWhile;

public class While_Loop_Intro {
    public static void main(String[] args) {


        if(true){
            System.out.println("Wooden Spoon");//1 time
        }

        System.out.println("--------------------------");

        while(true){
            System.out.println("Wooden Spoon"); // repeats until condition will get false
        }





    }
}
